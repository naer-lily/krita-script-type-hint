SKIP_CLASSES = {"KisSliderSpinBoxPrivate"}

# Classes that are base dependencies — always output first
PRIORITY_BASES = ["Node", "Shape"]


# ---------------------------------------------------------------------------
# Doxygen XML → plain text
# ---------------------------------------------------------------------------

def _doxy_text(elem: ET.Element | None, *, plain: bool = False) -> str:
    """Recursively extract text from a Doxygen XML element tree.

    When *plain* is True, no markdown formatting (backticks, links) is added.
    """
    if elem is None:
        return ""
    tag = elem.tag

    if tag == "text":
        return elem.text or ""

    if tag in ("briefdescription", "detaileddescription", "inbodydescription",
               "highlight", "verbatim", "codeline"):
        return _gos(elem, plain=plain)

    if tag == "para":
        inner = _gos(elem, plain=plain).strip()
        return f"\n{inner}\n" if inner else ""

    if tag == "ref":
        name = _gos(elem, plain=plain)
        return name if plain else f"`{name}`"

    if tag in ("emphasis", "computeroutput", "bold"):
        inner = _gos(elem, plain=plain)
        return inner if plain else f"`{inner}`"

    if tag == "programlisting":
        lines = []
        for child in elem:
            if child.tag == "codeline":
                lines.append(_gos(child, plain=plain))
        code = "\n".join(lines).strip()
        return f"\n```\n{code}\n```\n" if code else ""

    if tag == "itemizedlist":
        return "\n" + _gos(elem, plain=plain)

    if tag == "listitem":
        inner = _gos(elem, plain=plain).strip()
        return f"- {inner}\n" if inner else ""

    if tag == "simplesect":
        kind = elem.get("kind", "")
        if kind == "return":
            return ":returns: " + _gos(elem, plain=plain).strip()
        if kind == "see":
            see = _gos(elem, plain=plain).strip()
            return f"\nSee also: {see}" if see else ""
        if kind == "note":
            return "\nNote: " + _gos(elem, plain=plain).strip()
        if kind == "warning":
            return "\nWarning: " + _gos(elem, plain=plain).strip()
        if kind == "since":
            return "\nSince: " + _gos(elem, plain=plain).strip()
        return ""

    if tag == "parameterlist":
        return ""  # handled separately

    if tag == "parameteritem":
        return ""  # handled separately

    if tag == "ulink":
        url = elem.get("url", "")
        text = _gos(elem, plain=plain)
        return text if plain else (f"[{text}]({url})" if url else text)

    if tag == "sp":
        return " "

    if tag == "ndash":
        return "—"

    if tag == "linebreak":
        return "\n"

    if tag == "hruler":
        return "\n---\n"

    # fallback for unknown tags
    return _gos(elem, plain=plain)


def _gos(elem: ET.Element | None, *, plain: bool = False) -> str:
    """Walk all children, extract text, and smart-concatenate."""
    if elem is None:
        return ""
    children = list(elem)
    if not children:
        return elem.text or ""
    parts = []
    if elem.text:
        parts.append(elem.text)
    for child in children:
        parts.append(_doxy_text(child, plain=plain))
        if child.tail:
            parts.append(child.tail)
    result = "".join(parts)
    return result


def _extract_param_docs(detail: ET.Element | None) -> dict[str, str]:
    """Extract parameter documentation from detaileddescription."""
    if detail is None:
        return {}
    param_docs: dict[str, str] = {}
    for item in detail.iter("parameteritem"):
        name_elem = item.find("parameternamelist/parametername")
        desc_elem = item.find("parameterdescription")
        if name_elem is not None and desc_elem is not None:
            name = _gos(name_elem).strip()
            desc = _gos(desc_elem).strip()
            if name and desc:
                param_docs[name] = desc
    return param_docs


def make_docstring(brief: ET.Element | None, detail: ET.Element | None,
                   param_docs: dict[str, str] | None = None) -> str:
    """Build a clean reST-style docstring from Doxygen brief + detail."""
    brief_text = _gos(brief).strip() if brief is not None else ""
    detail_text = _gos(detail).strip() if detail is not None else ""

    # Extract parameter list from detail (it gets removed by _doxy_text)
    raw_param_docs = _extract_param_docs(detail)

    sections: list[str] = []

    if brief_text:
        sections.append(brief_text)

    # Clean detail: remove leading/trailing blank lines
    if detail_text:
        # Merge brief into the start of detail if brief already said the same thing
        cleaned = _clean_docstring(detail_text)
        if cleaned and cleaned != brief_text:
            sections.append(cleaned)

    # Parameter docs
    pd = param_docs or raw_param_docs
    if pd:
        param_block = "\n".join(
            f":param {name}: {desc}" for name, desc in pd.items()
        )
        sections.append(param_block)

    return "\n\n".join(sections)


def _clean_docstring(text: str) -> str:
    """Normalise whitespace, strip @brief, and remove redundant blank lines."""
    text = re.sub(r'^\s*@brief\s+', '', text)
    lines = text.splitlines()
    # Remove leading/trailing blank lines
    while lines and not lines[0].strip():
        lines.pop(0)
    while lines and not lines[-1].strip():
        lines.pop()
    # Collapse 3+ blank lines into 2
    cleaned: list[str] = []
    blank_count = 0
    for line in lines:
        if not line.strip():
            blank_count += 1
            if blank_count <= 2:
                cleaned.append("")
        else:
            blank_count = 0
            cleaned.append(line)
    return "\n".join(cleaned)


def indent(text: str, level: int = 1) -> str:
    """Indent each line of *text* by *level* × 4 spaces."""
    prefix = " " * (level * 4)
    return "\n".join(prefix + line if line.strip() else ""
                     for line in text.splitlines())


# ---------------------------------------------------------------------------
# C++ → Python type conversion
# ---------------------------------------------------------------------------

def _cpp_to_py_type(cpp_type_raw: str) -> str:
    """Convert a C++ type string to a Python type annotation."""
    t = cpp_type_raw.strip()
    # Remove cv-qualifiers and indirection
    t = re.sub(r'\bconst\b\s*', '', t)
    t = t.replace('*', '').replace('&', '').strip()

    # Strip Qt macros
    t = re.sub(r'\bQ_DECL_DEPRECATED\b', '', t).strip()

    # Normalise C++ scoped names: Qt::Orientations → Qt.Orientations
    t = re.sub(r'(\w+)::(\w+)', r'\1.\2', t)

    if not t or t == 'void':
        return 'None'

    # Template types — handle QList<T>, QMap<K,V>, QVector<T>
    # Must be done before word-boundary replacements
    t = re.sub(r'\bQList\s*<\s*(.+?)\s*>', r'list[\1]', t)
    t = re.sub(r'\bQMap\s*<\s*(.+?)\s*,\s*(.+?)\s*>', r'dict[\1, \2]', t)
    t = re.sub(r'\bQVector\s*<\s*(.+?)\s*>', r'list[\1]', t)
    t = re.sub(r'\bQPair\s*<\s*(.+?)\s*,\s*(.+?)\s*>', r'tuple[\1, \2]', t)

    # Now simple replacements
    replacements = [
        (r'\bQString\b', 'str'),
        (r'\bQStringList\b', 'list[str]'),
        (r'\bQByteArray\b', 'QByteArray'),
        (r'\bQVariant\b', 'Any'),
        (r'\bqreal\b', 'float'),
        (r'\bdouble\b', 'float'),
        (r'\bfloat\b', 'float'),
        (r'\bbool\b', 'bool'),
        (r'\bunsigned\s+int\b', 'int'),
        (r'\bint\b', 'int'),
        (r'\bqint32\b', 'int'),
        (r'\bQObject\b', 'QObject'),
        (r'\bQColor\b', 'QColor'),
        (r'\bQRect\b', 'QRect'),
        (r'\bQRectF\b', 'QRectF'),
        (r'\bQPoint\b', 'QPoint'),
        (r'\bQPointF\b', 'QPointF'),
        (r'\bQTransform\b', 'QTransform'),
        (r'\bQIcon\b', 'QIcon'),
        (r'\bQImage\b', 'QImage'),
        (r'\bQUuid\b', 'QUuid'),
        (r'\bQWidget\b', 'QWidget'),
        (r'\bQDockWidget\b', 'QDockWidget'),
        (r'\bQMainWindow\b', 'QMainWindow'),
        (r'\bQAction\b', 'QAction'),
    ]
    for pattern, repl in replacements:
        t = re.sub(pattern, repl, t)

    # Clean up leftover whitespace
    return re.sub(r'\s+', ' ', t).strip()


def convert_type(cpp_type_raw: str) -> tuple[str, set[str]]:
    """Convert C++ type and return (py_type, {referenced_type_names}).

    Referenced type names are used to track which Qt/typing imports are needed.
    """
    py = _cpp_to_py_type(cpp_type_raw)
    refs: set[str] = set()
    # Collect known Qt type references
    for qt_type in QT_MODULES:
        if re.search(r'\b' + re.escape(qt_type) + r'\b', py):
            refs.add(qt_type)
    return py, refs


def convert_default_value(cpp_val: str, param_type_cpp: str) -> str | None:
    """Convert a C++ default value to Python literal, or None if empty."""
    cpp_val = cpp_val.strip()
    if not cpp_val:
        return None
    param_type_py, _ = convert_type(param_type_cpp)

    # QString() or QString("xxx")
    m = re.match(r'QString\(\s*"(.*)"\s*\)', cpp_val)
    if m:
        return repr(m.group(1))

    # QString() without arguments → empty string
    if re.match(r'QString\(\s*\)', cpp_val):
        return '""'

    # QPointF(x, y)
    m = re.match(r'QPointF\((.+)\)', cpp_val)
    if m:
        return f"QPointF({m.group(1)})"

    # Numeric defaults
    if cpp_val == '0' and param_type_py not in ('int', 'float', 'bool'):
        return 'None'
    if cpp_val.isdigit() or (cpp_val.startswith('-') and cpp_val[1:].isdigit()):
        return cpp_val
    if cpp_val.replace('.', '', 1).replace('-', '', 1).isdigit():
        return cpp_val

    # Booleans
    if cpp_val == 'true':
        return 'True'
    if cpp_val == 'false':
        return 'False'
    if cpp_val == 'nullptr':
        return 'None'

    # Strip surrounding quotes → Python string literal
    if cpp_val.startswith('"') and cpp_val.endswith('"'):
        return repr(cpp_val[1:-1])
    if cpp_val.startswith("'") and cpp_val.endswith("'"):
        return repr(cpp_val[1:-1])

    # Resolve known namespace variable references to their string values
    NS_DEFAULTS = {
        "PaintingResources::defaultStrokeStyle": '"ForegroundColor"',
        "PaintingResources::defaultFillStyle": '"None"',
    }
    if cpp_val in NS_DEFAULTS:
        return NS_DEFAULTS[cpp_val]

    # Normalise C++ scoped references: Namespace::value → Namespace.value
    cpp_val = re.sub(r'(\w+)::(\w+)', r'\1.\2', cpp_val)

    # Enum-like defaults: keep as-is (they'll be class attributes)
    return cpp_val


