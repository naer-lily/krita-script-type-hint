class Member(NamedTuple):
    name: str
    kind: str          # "method" | "constructor" | "signal" | "static_method"
    return_type: str
    params: list[tuple[str, str, str | None]]  # (name, type, default)
    doc: str
    is_static: bool


class EnumValue(NamedTuple):
    name: str
    value: str | None


@dataclasses.dataclass
class EnumDef:
    name: str
    values: list[EnumValue]


@dataclasses.dataclass
class ClassDef:
    name: str
    bases: list[str]
    doc: str
    members: list[Member]
    enums: list[EnumDef]


class NamespaceDef(NamedTuple):
    name: str
    doc: str
    variables: list[tuple[str, str]]     # (name, py_type)
    functions: list[Member]


def _doc_text_of(elem: ET.Element | None) -> str:
    """Extract markdown-formatted text (for docstrings)."""
    return _gos(elem).strip() if elem is not None else ""


def _param_name(param_elem: ET.Element, counter: int) -> str:
    """Extract parameter name, fall back to `arg{counter}` if missing."""
    decl = param_elem.find("declname")
    if decl is not None:
        name = _gos(decl, plain=True).strip()
        if name:
            return name
    return f"arg{counter}"


def _type_text_of(elem: ET.Element | None) -> str:
    """Extract plain text WITHOUT markdown (for type annotations)."""
    return _gos(elem, plain=True).strip() if elem is not None else ""


def extract_members(compound: ET.Element, class_name: str) -> list[Member]:
    """Extract public methods/signals/slots from a compounddef element."""
    members: list[Member] = []

    for memberdef in compound.findall(".//memberdef"):
        kind = memberdef.get("kind", "")
        prot = memberdef.get("prot", "")
        if prot != "public":
            continue

        # Methods, slots, signals
        if kind not in ("function", "slot", "signal"):
            continue

        name_elem = memberdef.find("name")
        if name_elem is None:
            continue
        name = _type_text_of(name_elem)

        # Skip destructors, operators, and Qt-internal methods
        if '~' in name or 'operator' in name.lower():
            continue

        is_static = memberdef.get("static", "no") == "yes"

        if kind == "signal":
            # Build param list for signal
            params: list[tuple[str, str, str | None]] = []
            for i, param in enumerate(memberdef.findall("param")):
                ptype = _type_text_of(param.find("type"))
                ptype_py, _ = convert_type(ptype)
                pname = _param_name(param, i)
                params.append((pname, ptype_py, None))
            brief = memberdef.find("briefdescription")
            detail = memberdef.find("detaileddescription")
            doc = make_docstring(brief, detail)
            members.append(Member(
                name=name, kind="signal", return_type="",
                params=params, doc=doc, is_static=False
            ))
            continue

        # Function or slot
        ret_type_elem = memberdef.find("type")
        ret_type_cpp = _type_text_of(ret_type_elem) if ret_type_elem is not None else ""
        ret_type_py, _ = convert_type(ret_type_cpp) if ret_type_cpp else ("None", set())

        is_constructor = (name == class_name)

        params: list[tuple[str, str, str | None]] = []
        for i, param in enumerate(memberdef.findall("param")):
            ptype_cpp = _type_text_of(param.find("type"))
            ptype_py, _ = convert_type(ptype_cpp)
            pname = _param_name(param, i)
            defval_elem = param.find("defval")
            defval = None
            if defval_elem is not None:
                defval = convert_default_value(_type_text_of(defval_elem), ptype_cpp)
            params.append((pname, ptype_py, defval))

        brief = memberdef.find("briefdescription")
        detail = memberdef.find("detaileddescription")
        doc = make_docstring(brief, detail)

        kind_str = "constructor" if is_constructor else ("static_method" if is_static else "method")
        members.append(Member(
            name=name, kind=kind_str,
            return_type=ret_type_py if ret_type_py != "void" else "None",
            params=params, doc=doc, is_static=is_static
        ))

    return members


def extract_enums(compound: ET.Element) -> list[EnumDef]:
    """Extract public enums from a compounddef."""
    enums: list[EnumDef] = []
    for enum_elem in compound.findall(".//memberdef[@kind='enum']"):
        if enum_elem.get("prot", "") != "public":
            continue
        name_elem = enum_elem.find("name")
        if name_elem is None:
            continue
        enum_name = _type_text_of(name_elem)
        values: list[EnumValue] = []
        for ev in enum_elem.findall("enumvalue"):
            ev_name = _type_text_of(ev.find("name")) if ev.find("name") is not None else ""
            ev_initializer = ev.find("initializer")
            ev_val = _type_text_of(ev_initializer) if ev_initializer is not None else None
            values.append(EnumValue(name=ev_name, value=ev_val))
        enums.append(EnumDef(name=enum_name, values=values))
    return enums


def parse_class_xml(filepath: Path) -> ClassDef | None:
    """Parse a class_*.xml file and return a ClassDef, or None if skipped."""
    tree = ET.parse(filepath)
    root = tree.getroot()
    compound = root.find(".//compounddef[@kind='class']")
    if compound is None:
        return None

    name = _type_text_of(compound.find("compoundname"))
    if name in SKIP_CLASSES:
        return None

    # Parent classes
    bases: list[str] = []
    for base in compound.findall("basecompoundref"):
        base_name = _type_text_of(base)
        if base_name and not base_name.startswith("Q"):
            bases.append(base_name)

    # Doc
    brief = compound.find("briefdescription")
    detail = compound.find("detaileddescription")
    class_doc = make_docstring(brief, detail)

    # Members
    members = extract_members(compound, name)

    # Enums
    enums = extract_enums(compound)

    return ClassDef(name=name, bases=bases, doc=class_doc,
                    members=members, enums=enums)


def parse_namespace_xml(filepath: Path) -> NamespaceDef | None:
    """Parse a namespace_*.xml file."""
    tree = ET.parse(filepath)
    root = tree.getroot()
    compound = root.find(".//compounddef[@kind='namespace']")
    if compound is None:
        return None

    name = _type_text_of(compound.find("compoundname"))

    brief = compound.find("briefdescription")
    detail = compound.find("detaileddescription")
    doc = make_docstring(brief, detail)

    variables: list[tuple[str, str]] = []
    functions: list[Member] = []

    for memberdef in compound.findall(".//memberdef"):
        kind = memberdef.get("kind", "")
        prot = memberdef.get("prot", "")

        name_elem = memberdef.find("name")
        if name_elem is None:
            continue
        member_name = _type_text_of(name_elem)

        if kind == "variable" and prot == "public":
            ptype_cpp = _type_text_of(memberdef.find("type"))
            ptype_py, _ = convert_type(ptype_cpp)
            variables.append((member_name, ptype_py))

        elif kind == "function" and prot == "public":
            ret_type_cpp = _type_text_of(memberdef.find("type"))
            ret_type_py, _ = convert_type(ret_type_cpp) if ret_type_cpp else ("None", set())

            params: list[tuple[str, str, str | None]] = []
            for i, param in enumerate(memberdef.findall("param")):
                ptype = _type_text_of(param.find("type"))
                ptype_py, _ = convert_type(ptype)
                pname = _param_name(param, i)
                defval_elem = param.find("defval")
                defval = None
                if defval_elem is not None:
                    defval = convert_default_value(_gos(defval_elem).strip(), ptype)
                params.append((pname, ptype_py, defval))

            functions.append(Member(
                name=member_name, kind="function", return_type=ret_type_py,
                params=params, doc="", is_static=False
            ))

    return NamespaceDef(name=name, doc=doc, variables=variables, functions=functions)


# ---------------------------------------------------------------------------
# Collect opaque types (Kis*, Ko*, etc.)
# ---------------------------------------------------------------------------

def collect_opaque_types(classes: list[ClassDef],
                         namespaces: list[NamespaceDef]) -> set[str]:
    """Find all C++ internal types that are referenced but not defined as classes."""
    defined = {c.name for c in classes}
    defined |= {n.name for n in namespaces}
    defined |= set(QT_MODULES.keys())
    defined.add("Any")  # from typing

    # Always-declared opaque types (internal Krita C++ types used across classes)
    ALWAYS_OPAQUE = {
        "DockPosition", "Bicubic",
        "KisNodeList", "KisImageWSP", "KisFigurePaintingToolHelper",
        "KisNodeSP", "KisLayerSP", "KoCanvasBase", "KoChannelInfo",
        "KisImage", "KisImageSP", "KisCloneLayerSP", "KisDocument",
        "KisFileLayerSP", "KisColorizeMaskSP", "KisFilterMaskSP",
        "KisFilterConfigurationSP", "KisGeneratorLayerSP",
        "KisAdjustmentLayerSP", "KisGroupLayerSP", "KisShapeGroupSP",
        "KoShapeGroup", "KoColor", "KoResourceSP",
        "KisPropertiesConfigurationSP", "KisSelectionSP",
        "KisSelectionMaskSP", "KoShape", "KisTransformMaskSP",
        "KisTransparencyMaskSP", "KoShapeControllerBase", "KisView",
        "KisShapeLayerSP", "KisMainWindow", "KoCanvasObserverBase",
        "KoDockFactoryBase", "KisPresetChooser", "KisGridConfig",
        "KisGuidesConfig",
    }

    opaque: set[str] = set()
    pattern = re.compile(r'\b([A-Z][A-Za-z0-9_]+)\b')

    def scan(text: str) -> None:
        for m in pattern.finditer(text):
            word = m.group(1)
            if word not in defined and len(word) > 1:
                opaque.add(word)

    for cls in classes:
        for base in cls.bases:
            if base not in defined:
                opaque.add(base)
        for m in cls.members[:-1]:  # scan param/return types
            scan(m.return_type)
            for _, ptype, _ in (m.params or []):
                scan(ptype)

    # Filter: only keep types that look like Krita internals, plus always-needed ones
    detected = {t for t in opaque if t.startswith(("Kis", "Ko", "KUndo2"))}
    detected |= ALWAYS_OPAQUE
    return detected


# ---------------------------------------------------------------------------
# Topological sort
# ---------------------------------------------------------------------------

def topological_sort(classes: list[ClassDef]) -> list[ClassDef]:
    """Order classes so base classes come before their subclasses."""
    name_to_class = {c.name: c for c in classes}

    in_degree: dict[str, int] = {c.name: 0 for c in classes}
    children: dict[str, list[str]] = defaultdict(list)

    for cls in classes:
        for base in cls.bases:
            if base in name_to_class:
                in_degree[cls.name] += 1
                children[base].append(cls.name)

    # Start with classes that have no parent (zero in-degree), then priority bases
    queue: list[str] = [name for name, deg in in_degree.items() if deg == 0]
    # Move priority bases to front
    for pb in reversed(PRIORITY_BASES):
        if pb in queue:
            queue.remove(pb)
            queue.insert(0, pb)

    result: list[ClassDef] = []
    while queue:
        name = queue.pop(0)
        result.append(name_to_class[name])
        for child in children[name]:
            in_degree[child] -= 1
            if in_degree[child] == 0:
                queue.append(child)

    # Any remaining (cycles — shouldn't happen)
    for name, deg in in_degree.items():
        if deg > 0 and name not in [r.name for r in result]:
            result.append(name_to_class[name])

    return result


