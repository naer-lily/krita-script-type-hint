def _emit_param(pname: str, ptype: str, defval: str | None) -> str:
    """Format a single parameter for a method signature."""
    base = f"{pname}: {ptype}"
    if defval is not None:
        base += f" = {defval}"
    return base


def _emit_docstring(doc: str, level: int) -> str:
    """Emit a docstring block, or empty string if doc is empty."""
    if not doc.strip():
        return ""
    lines = ['"""', doc.rstrip(), '"""']
    return "\n".join(indent(line, level) for line in lines) + "\n"


def emit_method(member: Member, class_name: str = "") -> str:
    """Generate a single method definition for a .pyi stub."""
    parts: list[str] = []

    if member.kind == "signal":
        result = f"{member.name}: ClassVar[pyqtSignal]"
        if member.doc.strip():
            doc = member.doc.strip()
            if '\n' in doc:
                result += "\n" + _emit_docstring(doc, 0)
            else:
                result += f"  # {doc}"
        return result

    if member.kind == "static_method":
        parts.append("@staticmethod")

    params_strs: list[str] = []
    if member.kind != "static_method":
        params_strs.append("self")
    for pname, ptype, defval in (member.params or []):
        params_strs.append(_emit_param(pname, ptype, defval))

    func_name = "__init__" if member.kind == "constructor" else member.name
    ret = f" -> {member.return_type}"
    sig = f"def {func_name}({', '.join(params_strs)}){ret}:"

    parts.append(sig)

    doc_block = _emit_docstring(member.doc, 1)
    if doc_block:
        parts.append(doc_block)

    parts.append(indent("...", 1))
    return "\n".join(parts)


def emit_overload_group(name: str, overloads: list[Member]) -> str:
    """Emit an @overload group for methods with the same name."""
    lines: list[str] = []
    for member in overloads:
        lines.append("@overload")
        params_strs = ["self"]
        for pname, ptype, defval in (member.params or []):
            params_strs.append(_emit_param(pname, ptype, defval))
        ret = f" -> {member.return_type}"
        lines.append(f"def {name}({', '.join(params_strs)}){ret}: ...")
        lines.append("")
    return "\n".join(lines).rstrip()


def emit_enum(enum_def: EnumDef) -> str:
    """Emit an IntEnum class for a C++ enum."""
    lines = [f"class {enum_def.name}(int):"]
    for val in enum_def.values:
        if val.value is not None:
            lines.append(f"    {val.name} = {val.value}")
        else:
            lines.append(f"    {val.name}: ClassVar[{enum_def.name}]")
    return "\n".join(lines)


def emit_class_body(cls: ClassDef) -> str:
    """Generate the class definition body (no file header or imports)."""
    lines: list[str] = []

    bases_str = f"({', '.join(cls.bases)})" if cls.bases else ""
    lines.append(f"class {cls.name}{bases_str}:")

    if cls.doc.strip():
        lines.append(indent('"""', 1))
        lines.append(indent(cls.doc.rstrip(), 1))
        lines.append(indent('"""', 1))
        lines.append("")

    for enum in cls.enums:
        lines.append(indent(emit_enum(enum), 1))
        lines.append("")

    by_name: dict[str, list[Member]] = defaultdict(list)
    for m in cls.members:
        by_name[m.name].append(m)

    def sort_key(name_members: tuple[str, list[Member]]) -> tuple[int, str]:
        name, _members = name_members
        if name == "__init__" or any(m.kind == "constructor" for m in _members):
            return (0, name)
        if all(m.kind == "signal" for m in _members):
            return (2, name)
        return (1, name)

    for name, group in sorted(by_name.items(), key=sort_key):
        non_signal = [m for m in group if m.kind != "signal"]
        signals_in_group = [m for m in group if m.kind == "signal"]

        if len(non_signal) > 1:
            lines.append(indent(emit_overload_group(name, non_signal), 1))
            lines.append("")
        elif len(non_signal) == 1:
            lines.append(indent(emit_method(non_signal[0], cls.name), 1))
            lines.append("")

        for sig in signals_in_group:
            lines.append(indent(emit_method(sig, cls.name), 1))
            lines.append("")

    result = "\n".join(lines).rstrip()

    # Qualify unqualified enum references
    for enum in cls.enums:
        for sep in (" ", "(", ","):
            result = result.replace(f"{sep}{enum.name}.", f"{sep}{cls.name}.{enum.name}.")
        result = result.replace(f"ClassVar[{enum.name}]", f"ClassVar[{cls.name}.{enum.name}]")

    return result


