def _class_to_filename(name: str) -> str:
    """Convert ClassName to class_name (snake_case)."""
    result: list[str] = []
    for i, ch in enumerate(name):
        if ch.isupper() and i > 0:
            result.append('_')
        result.append(ch.lower())
    return ''.join(result)


