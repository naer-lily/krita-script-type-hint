"""Generate krita.pyi from Doxygen XML documentation.

Parses class_*.xml and namespace_*.xml files (generated by Doxygen from Krita's
libkis C++ headers) and produces a clean, Pythonic type stub (.pyi) file for
IDE auto-completion when writing Krita plugins.
"""

